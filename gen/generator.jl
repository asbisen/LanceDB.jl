# Regenerate src/api.jl from the vendored lancedb.h header.
# Run: julia --project=gen gen/generator.jl
#
# After regeneration, the hand-written placeholder in src/api.jl is replaced.
# Arrow C ABI struct layouts defined in src/arrow_abi.jl are NOT generated —
# they are maintained manually to match the Arrow C Data Interface spec.

using Clang.Generators

HEADER = joinpath(@__DIR__, "lancedb.h")

if !isfile(HEADER)
    error("""
    gen/lancedb.h not found.
    Copy the header from the desired lancedb-c release:
        cp path/to/lancedb-c/include/lancedb.h gen/lancedb.h
    """)
end

OUTPUT = joinpath(@__DIR__, "..", "src", "api.jl")

cd(@__DIR__) do
    args = get_default_args()
    ctx  = create_context([HEADER], args)
    build!(ctx)
end

# Prepend the required constant reference and auto-gen header
lines = readlines(OUTPUT)
header = """
# AUTO-GENERATED by gen/generator.jl — do not edit by hand.
# Regenerate with: julia --project=gen gen/generator.jl
# Requires liblancedb constant defined in LanceDB.jl module.

"""
write(OUTPUT, header * join(lines, "\n"))
println("Generated: $OUTPUT")
